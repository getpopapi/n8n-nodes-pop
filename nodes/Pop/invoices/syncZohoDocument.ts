/**
 * Sync Document to Zoho
 *
 * Delegates invoice/credit-note creation to Zoho Books/Invoice via the POP API's
 * native Zoho connector. All Zoho-side mapping (tax, customer, payment terms,
 * document status) is handled server-side in pop-cloud-api — this operation only
 * gates the request on the connector being active, then forwards the POP payload.
 *
 * Flow:
 * 1. (optional) GET integration/zoho/status — verify the Zoho connector is active
 *    for this account before attempting to sync
 * 2. POST integration/zoho/sync — forward the POP payload; the backend maps it
 *    to a Zoho invoice (TD01) or credit note (TD04)
 *
 * Supports passthrough, form, and JSON input modes (no raw — the endpoint only
 * accepts JSON).
 *
 * POP API endpoints: GET /integration/zoho/status, POST /integration/zoho/sync
 */
