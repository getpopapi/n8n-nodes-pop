/**
 * Operation handler — verifies the Zoho connector status (optional), then
 * builds and sends the integration/zoho/sync request.
 *
 * Depending on the selected input mode:
 * - passthrough: forwards the incoming n8n item JSON directly
 * - form: assembles the payload using buildInvoicePayload() with variant='zoho'
 * - json: uses the user-provided JSON body as-is
 *
 * The response distinguishes the status-check outcome from the sync outcome
 * so downstream nodes can branch on either without losing the raw POP response.
 */
export async function handler(
	this: IExecuteFunctions,
	params: {
		baseUrl?: string;
		path: string;
		statusPath: string;
		checkConnectorStatus: 'yes' | 'no';
		inputMode: 'form' | 'json' | 'passthrough';
		jsonBody?: object;
		headers?: Record<string, string>;
		_itemIndex?: number;
	} & Partial<InvoiceFormParams>,
): Promise<unknown> {
	const { baseUrl, path, statusPath, checkConnectorStatus, inputMode, headers } = params;
	const itemIndex = params._itemIndex ?? 0;

	const sharedHeaders: Record<string, string> = { ...(headers ?? {}) };
	const formKey = (params.licenseKey ?? '').trim();
	if (formKey) {
		sharedHeaders['X-API-Key'] = formKey;
	}

	let connectorActive: boolean | null = null;
	let statusChecked = false;

	if (checkConnectorStatus === 'yes') {
		statusChecked = true;
		const statusResponse = (await popRequest.call(
			this,
			{ url: statusPath, method: 'GET', headers: { ...sharedHeaders } },
			baseUrl,
		)) as { data?: { active_connector?: string; zoho_connected?: boolean } };

		connectorActive =
			statusResponse?.data?.active_connector === 'zoho' && statusResponse?.data?.zoho_connected === true;

		if (!connectorActive) {
			throw new NodeOperationError(
				this.getNode(),
				'Zoho integration is not active for this account. Enable it from the POP account panel before using this action.',
				{ itemIndex },
			);
		}
	}

	const requestOptions: PopRequestOptions = {
		url: path,
		method: 'POST',
		headers: { ...sharedHeaders },
	};

	if (inputMode === 'passthrough') {
		// Forward the incoming item's JSON data directly to the API
		requestOptions.json = true;
		requestOptions.body = { ...this.getInputData()[itemIndex].json };
	} else if (inputMode === 'form') {
		// Build the structured payload from form field values using the Zoho variant.
		// sendInvoice is always false — Zoho sync has no integration.use/action toggle.
		requestOptions.json = true;
		requestOptions.body = buildInvoicePayload(params as InvoiceFormParams, 'zoho', false);
	} else {
		// Use the manually provided JSON body
		requestOptions.json = true;
		requestOptions.body = (params.jsonBody ?? {}) as Record<string, unknown>;
	}

	const syncResponse = await popRequest.call(this, requestOptions, baseUrl);

	return {
		connector_active: connectorActive,
		status_checked: statusChecked,
		sync_attempted: true,
		response: syncResponse,
	};
}
